import SwiftUI

extension Color {
    
    static func background() -> Color {
        return Color("BackgroundColor")
    }
    
    static func search() -> Color {
        return Color("SearchColor")
    }
    
    static func text() -> Color {
        return Color("TextColor")
    }
}
